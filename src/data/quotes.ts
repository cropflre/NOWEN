// 当前使用的名言列表（可被后端数据覆盖）
let activeQuotes = [...wisdomQuotes];

// 设置名言列表
export function setActiveQuotes(customQuotes: string[], useDefault: boolean) {
  if (useDefault) {
    // 合并系统默认名言和自定义名言
    const combined = [...wisdomQuotes];
    if (customQuotes && customQuotes.length > 0) {
      customQuotes.forEach((q) => {
        if (!combined.includes(q)) {
          combined.push(q);
        }
      });
    }
    activeQuotes = combined;
  } else {
    // 仅使用自定义名言
    if (customQuotes && customQuotes.length > 0) {
      activeQuotes = customQuotes;
    } else {
      // 如果没有自定义名言，回退到系统默认
      activeQuotes = [...wisdomQuotes];
    }
  }
}

// 获取随机名言（不限制，每次随机）
export function getRandomWisdom(): string {
  return activeQuotes[Math.floor(Math.random() * activeQuotes.length)];
}

// 处理名言更新的回调（供外部组件调用）
export function handleQuotesChange(customQuotes: string[], useDefault: boolean) {
  setActiveQuotes(customQuotes, useDefault);
}
